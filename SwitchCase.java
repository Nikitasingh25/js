public class SwitchCase {
    public static void main(String[] args) {
        int a = 8;
        switch (a) {
            case 1:
            case 2:
            case 3:
            case 4:
                System.out.println("Hello.");
                break;
            case 5:
                System.out.println("Hiii");
                break;
            default:
                System.out.println("Byeee");
        }
    }
}
